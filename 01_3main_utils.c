int	check_env(char **cmd, int **i)
{
	if (cmd[(*i)[0]][(*i)[1]] && cmd[(*i)[0]][(*i)[1]] != '\'')
		return (1);
	if (cmd[(*i)[0]][(*i)[1]] != '\"' && cmd[(*i)[0]][(*i)[1]] != '$')
		return (1);
	if (cmd[(*i)[0]][(*i)[1]] != ' ' && cmd[(*i)[0]][(*i)[1]] != '?')
		return (1);
	return (0);
}
